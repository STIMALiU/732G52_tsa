# vi skapar ts-objekt med ts()


#-------------------------------------------------------------------------------
# simulera lite data
x1<-c(4,5,7,5,8,12,20,17,15,16,19,22,25,26,22,15,11,16,20,26)
?ts
# skapar årsdata:
y1<-ts(data = x1,start = 2009,frequency = 1)
plot(y1)

# skapar kvartalsdata:
y2<-ts(data = x1,start = 2009,frequency = 4)
plot(y2)

